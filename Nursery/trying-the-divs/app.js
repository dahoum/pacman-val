    function createBoard() {

        var spritesheet = document.getElementById("spritesheet");
        let right = 0;
        let left = 425;
        let top = 0;
        let bottom = 478;

        // Cycle through the rows
        for (let i = 0; i < layout.length; i++) {
            // Clycle through the columns of one row
            for (let j = 0; j < layout[i].length; j++) {
                // Create a div
                var element = document.createElement("div");
                // Gives the div a class
                element.classList.add('container')
                // Creates the image
                var image = document.createElement("img")
                image.classList.add("clip")
                image.src = "img.png"
                element.appendChild(image)

                // why counter and not another letter
                let counter = 0;
                if (i === 0) {
                    if (counter < j) {
                        right += 20
                        left -= 20;
                        image.style.clipPath = `inset(0px ${left}px 478px ${right}px)`
                        image.style.position = 'relative'
                        counter++
                    } else if (counter === j) {
                        image.style.clipPath = `inset(0px 428px 478px 0px)`
                    }


                }

                if (i === 1) {
                    if (counter <= j) {
                        top += 20
                        bottom -= 20;
                        image.style.clipPath = `inset(${top}px 425px ${bottom}px 0px)`
                        counter++
                    }
                }

                spritesheet.appendChild(element);
            }

        }

    }
