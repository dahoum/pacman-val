function keyDown(e) {
    // up
    if (e.keyCode == 38) {
        upPressed = 1;
        if (curentMovingDirection == MovingDirection.down) {
            this.curentMovingDirection = MovingDirection.up
        }
        this.requectMovingDirection = MovingDirection.up

    }

    // down
    if (e.keyCode == 40) {
        downPressed = 1;
        if (curentMovingDirection == MovingDirection.up) {
            this.curentMovingDirection = MovingDirection.down
        }
        this.requectMovingDirection = MovingDirection.down
    }

    // left
    if (e.keyCode == 37) {
        leftPressed = 1;
        if (curentMovingDirection == MovingDirection.right) {
            this.curentMovingDirection = MovingDirection.left
        }
        this.requectMovingDirection = MovingDirection.left
    }

    // right
    if (e.keyCode == 39) {
        rightPressed = 1;
        if (curentMovingDirection == MovingDirection.right) {
            this.curentMovingDirection = MovingDirection.left
        }
        this.requectMovingDirection = MovingDirection.left
    }

}

function move() {

    // console.log(xpos);
    // console.log(ypos);
    if (curentMovingDirection !== this.requectMovingDirection) {
        if (Number.isInteger(this.x / 8.82) && Number.isInteger(this.y / 8)) {
            this.curentMovingDirection = this.requectMovingDirection
        }
    }

    switch (this.curentMovingDirection) {
        case MovingDirection.up:
            this.ypos -= maxSpeed
            break
        case MovingDirection.down:
            this.ypos += maxSpeed
            break
        case MovingDirection.left:
            this.xpos -= maxSpeed
            break
        case MovingDirection.right:
            this.xpos += maxSpeed
            break

    }
}

